/**
 * Constants represents the COBOL main supported statement types.
 */
public enum StatementType {
    /**
     * Constant for the GroupDataDeclarationCobolStatement
     */
    GROUP_STATEMENT,
    /**
     * Constant for the IntegerDeclarationCobolStatement
     */
    INTEGER_STATEMENT,
    /**
     * Constant for the AlphanumericDeclarationCobolStatement
     */
    ALPHANUMERIC_STATEMENT,

    /**
     * Constant for the CompDeclarationCobolStatement
     */
    COMP_STATEMENT
}
